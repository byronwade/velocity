function route(prompt: string): { kind: string; make: (p: string) => { title: string; entry: string } } {
	const p = prompt.toLowerCase();
	if (/(todo|task|checklist|to-do)/.test(p)) {
		return { kind: 'todo app', make: todo };
	}
	if (/(dashboard|admin|analytics|metrics|chart)/.test(p)) {
		return { kind: 'dashboard', make: dashboard };
	}
	if (/(landing|marketing|saas|startup|waitlist|hero|product page)/.test(p)) {
		return { kind: 'landing page', make: landing };
	}
	return { kind: 'starter', make: starter };
}

/** Route a prompt to a template and emit real, self-contained files. */
export function generate(prompt: string): Generated {
	const { kind, make } = route(prompt);
	const { entry } = make(prompt);
	const slug = slugify(prompt || kind);
	const dir = `builds/${slug}`;
	const readme = `# ${slug}\n\nGenerated by Velocity from the prompt:\n\n> ${prompt.trim() || '(empty)'}\n\nKind: ${kind}. Open index.html in the editor, or preview it live.\n`;
	return {
		name: slug,
		slug,
		kind,
		entry,
		files: {
			[`${dir}/index.html`]: entry,
			[`${dir}/README.md`]: readme,
		},
	};
}
