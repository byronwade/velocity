// ---------------------------------------------------------------------------
// Generator — the "describe an app → get files" seam (Builder mode).
//
// Today it is a deterministic, template-based scaffolder: a prompt is routed to
// a template by keyword, and it emits REAL, self-contained files that render in
// the preview with no build step and are editable/browsable like any workspace
// file. The signature (prompt → files) is exactly what an LLM code-gen backend
// would fill; swapping this function for a model call is the only change needed.
// ---------------------------------------------------------------------------

export interface Generated {
	name: string;
	slug: string;
	kind: string;
	/** All files written to the workspace, path → contents. */
	files: Record<string, string>;
	/** The self-contained HTML entry used for the live preview. */
	entry: string;
}

function slugify(s: string): string {
	return (
		s
			.toLowerCase()
			.replace(/[^a-z0-9]+/g, '-')
			.replace(/^-+|-+$/g, '')
			.slice(0, 32) || 'app'
	);
}

